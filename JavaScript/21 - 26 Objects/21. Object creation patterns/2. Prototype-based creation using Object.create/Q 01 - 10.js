const animalPrototype={
    speak(){
        console.log(`${this.name} ${this.sound}`);
        
    }
}

const a1=Object.create(animalPrototype)

a1.name="dog"
a1.sound="bark"
a1.speak()
const a2=Object.create(animalPrototype)

a2.name="cat"
a2.sound="meow"
a2.speak()
